!< VTK file XMl write ascii local.
module vtk_file_xml_write_ascii_local_state
!-----------------------------------------------------------------------------------------------------------------------------------
!< VTK file XMl write ascii local.
!-----------------------------------------------------------------------------------------------------------------------------------
use vtk_file_abstract_xml_write
!-----------------------------------------------------------------------------------------------------------------------------------

!-----------------------------------------------------------------------------------------------------------------------------------
implicit none
private
public :: xml_write_ascii_local_state
!-----------------------------------------------------------------------------------------------------------------------------------

!-----------------------------------------------------------------------------------------------------------------------------------
type, extends(abstract_xml_write_state) :: xml_write_ascii_local_state
  !< VTK file XML write ascii local state.
  contains
    procedure, pass :: write => write_ascii_local !< Write VTK file data.
endtype xml_write_ascii_local_state
!-----------------------------------------------------------------------------------------------------------------------------------
contains
  subroutine  write_ascii_local(self, xml_write)
  !---------------------------------------------------------------------------------------------------------------------------------
  !< Write XML data in ascii local format.
  !---------------------------------------------------------------------------------------------------------------------------------
  class(xml_write_ascii_local_state), intent(inout) :: self      !< Write state object.
  class(abstract_xml_write),          intent(inout) :: xml_write !< Write object.
  !---------------------------------------------------------------------------------------------------------------------------------

  !---------------------------------------------------------------------------------------------------------------------------------
  return
  !---------------------------------------------------------------------------------------------------------------------------------
  endsubroutine write_ascii_local
endmodule vtk_file_xml_write_ascii_local_state
